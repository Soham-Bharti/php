<?php

// PHP lcfirst() Function
// Converts the first character of a string to lowercase

// Syntax
// lcfirst(string)

$str = "Hello World!";
echo lcfirst($str) . "<br>"; // hello World!

$str = "HELLO World!";
echo lcfirst($str) . "<br>"; // hELLO World!

$str = "hello World!";
echo lcfirst($str) . "<br>"; // hello World!