<?php

// PHP asin() Function
// The asin() function returns the arc sine of a number.

// Tip: asin(1) returns the value of Pi/2.

// Syntax
// asin(number); -> Required. Specifies a number in range -1 to 1

echo (asin(0.64) . "<br>"); // 0.69449826562656
echo (asin(-0.4) . "<br>"); // -0.41151684606749
echo (asin(0) . "<br>"); // 0
echo (asin(-1) . "<br>"); // -1.5707963267949
echo (asin(1) . "<br>"); // 1.5707963267949
echo (asin(2)); // NAN


