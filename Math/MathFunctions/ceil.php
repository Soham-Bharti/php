<?php

// PHP ceil() Function
// The ceil() function rounds a number UP to the nearest integer, if necessary.

// Syntax
// ceil(number);

echo (ceil(0.60) . "<br>"); // 1
echo (ceil(0.40) . "<br>"); // 1
echo (ceil(5) . "<br>"); // 5
echo (ceil(5.1) . "<br>"); // 6
echo (ceil(-5.1) . "<br>"); // -5
echo (ceil(-5.9)); // -5
