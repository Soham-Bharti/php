<?php

// PHP acosh() Function
// The acosh() function returns the inverse hyperbolic cosine of a number.

// Syntax
// acosh(number); -> Required. Specifies a number


echo (acosh(7) . "<br>"); // 2.6339157938496
echo (acosh(1) . "<br>"); // 0
echo (acosh(2) . "<br>"); // 1.3169578969248
echo (acosh(56) . "<br>"); // 4.7184191423729
echo (acosh(2.45)) . "<br>"; // 1.5447131178707
echo (acosh(-2.45)) . "<br>"; // NAN
echo (acosh(-0.45)) . "<br>"; // NAN
