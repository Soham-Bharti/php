<?php


// PHP sinh() Function
// The sinh() function returns the hyperbolic sine of a number, which is equal to (exp(number) - exp(-number))/2).

echo (sinh(3) . "<br>"); // 10.01787492741
echo (sinh(-3) . "<br>"); // -10.01787492741
echo (sinh(0) . "<br>"); // 0
echo (sinh(M_PI) . "<br>"); // 11.548739357258
echo (sinh(M_PI_2)); // 2.3012989023073
