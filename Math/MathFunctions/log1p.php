<?php

// PHP log1p() Function
// The log1p() function returns log(1+number), computed in a way that is accurate even when the value of number is close to zero.


echo (log1p(2.7183) . "<br>"); // 1.3132665745863
echo (log1p(2) . "<br>"); // 1.0986122886681
echo (log1p(1) . "<br>"); // 0.69314718055995
echo (log1p(0)); // 0
