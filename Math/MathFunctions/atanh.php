<?php

// PHP atanh() Function
// The atanh() function returns the inverse hyperbolic tangent of a number.

// Syntax
// atanh(number); -> Required. Specifies a number

echo (atanh(M_PI_4) . "<br>"); // 1.0593061708232
echo (atanh(0.50) . "<br>"); // 0.54930614433405
echo (atanh(-0.50) . "<br>"); // -0.54930614433405
echo (atanh(1) . "<br>"); // INF
echo (atanh(-1)); // -INF
