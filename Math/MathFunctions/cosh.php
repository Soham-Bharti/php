<?php

// PHP cosh() Function
// The cosh() function returns the hyperbolic cosine of a number (equivalent to (exp(number) + exp(-number)) / 2).

// Syntax
// cosh(number);

echo (cosh(3) . "<br>"); // 10.067661995778
echo (cosh(-3) . "<br>"); // 10.067661995778
echo (cosh(0) . "<br>"); // 1
echo (cosh(M_PI) . "<br>"); // 11.591953275522
echo (cosh(2 * M_PI)); // 267.74676148375
