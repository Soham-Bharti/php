<?php

// PHP atan() Function
// The atan() function returns the arc tangent of arg as a numeric value between -Pi/2 and Pi/2 radians.

// Syntax
// atan(arg); -> Required. Specifies an argument to process


echo (atan(0.50) . "<br>"); // 0.46364760900081
echo (atan(0) . "<br>"); // 0 
echo (atan(-0.50) . "<br>"); // -0.46364760900081
echo (atan(5) . "<br>"); // 1.373400766945
echo (atan(-5) . "<br>"); // -1.373400766945
echo (atan(100) . "<br>"); // 1.5607966601082
echo (atan(-100)); // -1.5607966601082
