<?php

// PHP asinh() Function
// The asinh() function returns the inverse hyperbolic sine of a number.

// Syntax
// asinh(number); -> Required. Specifies a number

echo asinh(0) . "<br>"; // 0
echo asinh(7.89) . "<br>"; // 2.7627352663139
echo asinh(24) . "<br>"; // 3.8716347563877
echo asinh(-24) . "<br>"; // -3.8716347563877
