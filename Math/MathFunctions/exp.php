<?php

// PHP exp() Function
// The exp() function returns E raised to the power of x (Ex).

// 'E' is the base of the natural system of logarithms (approximately 2.718282) and x is the number passed to it.


// Syntax
// exp(x); 

echo (exp(2) . "<br>"); // 7.3890560989307
echo (exp(0) . "<br>"); // 1
echo (exp(1) . "<br>"); // 2.718281828459
echo (exp(10) . "<br>"); // 22026.465794807
echo (exp(4.8)); // 121.51041751873
