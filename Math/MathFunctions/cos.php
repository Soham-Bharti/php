<?php

// PHP cos() Function
// The cos() function returns the cosine of a number.

// Note: The cos() function returns a numeric value between -1 and 1, which represents the cosine of the angle.

// Syntax
// cos(number);

echo (cos(3) . "<br>"); // -0.98999249660045
echo (cos(-3) . "<br>"); // -0.98999249660045
echo (cos(0) . "<br>"); // 1
echo (cos(M_PI) . "<br>"); // -1
echo (cos(2 * M_PI)); // 1
