<?php

// PHP tanh() Function
// The tanh() function returns the hyperbolic tangent of a number, which is equal to sinh(x)/cosh(x).

echo (tanh(M_PI_4) . "<br>"); // 0.65579420263267
echo (tanh(0.50) . "<br>"); // 0.46211715726001
echo (tanh(-0.50) . "<br>"); // -0.46211715726001
echo (tanh(5) . "<br>"); // 0.9999092042626
echo (tanh(10) . "<br>"); // 0.99999999587769
echo (tanh(-5) . "<br>"); // -0.9999092042626
echo (tanh(-10)); // -0.99999999587769
