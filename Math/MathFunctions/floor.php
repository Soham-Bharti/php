<?php


// PHP floor() Function
// The floor() function rounds a number DOWN to the nearest integer, if necessary, and returns the result.



// Syntax
// floor(number);

echo (floor(0.60) . "<br>"); // 0
echo (floor(0.40) . "<br>"); // 0
echo (floor(5) . "<br>"); // 5
echo (floor(5.1) . "<br>"); // 5
echo (floor(-5.1) . "<br>"); // -6
echo (floor(-5.9)); // -6